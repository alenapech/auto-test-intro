public class CreditParam {

    private String minSumm;

    private String maxSumm;

    private String percent;

    private String period;

    public String getMinSumm() {
        return minSumm;
    }

    public void setMinSumm(String minSumm) {
        this.minSumm = minSumm;
    }

    public String getMaxSumm() {
        return maxSumm;
    }

    public void setMaxSumm(String maxSumm) {
        this.maxSumm = maxSumm;
    }

    public String getPercent() {
        return percent;
    }

    public void setPercent(String percent) {
        this.percent = percent;
    }

    public String getPeriod() {
        return period;
    }

    public void setPeriod(String period) {
        this.period = period;
    }
}
